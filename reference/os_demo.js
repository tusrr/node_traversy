const os= require('os')

//platform
console.log(os.platform())

//cpu arch
console.log(os.arch())

//cpu core info
console.log(os.cpus())

//free mem
console.log(os.freemem())
//total mem
console.log(os.totalmem())
//home dir
console.log(os.homedir())
//up time
console.log(os.uptime())
//userinfo
console.log(os.userInfo())
//Hostname
console.log(os.hostname())
